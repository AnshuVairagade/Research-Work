def create_rttm(segments, audio_id, output_path):
    """Convert diarization results to RTTM format"""
    with open(output_path, 'w') as f:
        for seg in segments:
            start = seg['start_time']
            dur = seg['end_time'] - start
            line = f"SPEAKER {audio_id} 1 {start:.3f} {dur:.3f} <NA> <NA> {seg['speaker_label']} <NA> <NA>\n"
            f.write(line)

