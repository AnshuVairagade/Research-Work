class XVectorEmbedder:
    def __init__(self, device='cuda' if torch.cuda.is_available() else 'cpu'):
        self.device = device
        self.classifier = EncoderClassifier.from_hparams(
            source="speechbrain/spkrec-xvect-voxceleb",
            savedir="/content/pretrained_models/xvector",
            run_opts={"device": self.device}
        )
        self.target_sample_rate = 16000
        self.min_duration = 1.0

    def _preprocess_audio(self, waveform, sample_rate):

        if sample_rate != self.target_sample_rate:
            resampler = torchaudio.transforms.Resample(
                orig_freq=sample_rate,
                new_freq=self.target_sample_rate
            )
            waveform = resampler(waveform)


        waveform = waveform.mean(dim=0, keepdim=True) if waveform.shape[0] > 1 else waveform
        current_duration = waveform.shape[1] / self.target_sample_rate


        if current_duration < self.min_duration:
            pad_size = int((self.min_duration - current_duration) * self.target_sample_rate)
            waveform = torch.nn.functional.pad(waveform, (0, pad_size))

        return waveform.to(self.device)

    def create_embeddings(self, audio_paths):
        """Process audio files from Colab directory structure"""
        embeddings = []
        for path in tqdm(audio_paths, desc="Processing audio files"):
            try:
                waveform, sample_rate = torchaudio.load(path)
                waveform = self._preprocess_audio(waveform, sample_rate)
                with torch.no_grad():
                    emb = self.classifier.encode_batch(waveform)
                    emb = emb.squeeze().cpu().numpy()
                embeddings.append(emb)
            except Exception as e:
                print(f"Error processing {path}: {str(e)}")
                continue

        return np.array(embeddings)


