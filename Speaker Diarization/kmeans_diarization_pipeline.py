class SpeakerDiarizer:
    def __init__(self, pipeline_path, embedder, window_length=1.5, overlap=0.5):
        self.pipeline = joblib.load(pipeline_path)
        self.embedder = embedder
        self.window_length = window_length
        self.overlap = overlap
        self.sr = 16000
        self.label_map = {}

    def _time_to_samples(self, time):
        return int(time * self.sr)

    def process_audio(self, audio_path):
        """Core audio processing method that was missing"""
        # Load audio
        waveform, sample_rate = torchaudio.load(audio_path)
        waveform = torch.mean(waveform, dim=0) if waveform.dim() > 1 else waveform

        # Create segments
        window_samples = self._time_to_samples(self.window_length)
        hop_samples = self._time_to_samples(self.window_length - self.overlap)

        segments = []
        for start in range(0, waveform.shape[0], hop_samples):
            end = start + window_samples
            if end > waveform.shape[0]:
                break
            segment = waveform[start:end]
            segments.append({
                "start_time": start / sample_rate,
                "end_time": end / sample_rate,
                "waveform": segment.unsqueeze(0)
            })

        # Extract embeddings
        segment_embeddings = []
        for seg in segments:
            with torch.no_grad():
                emb = self.embedder.classifier.encode_batch(
                    seg["waveform"].to(self.embedder.device)
                )
                segment_embeddings.append(emb.squeeze().cpu().numpy())

        # Predict clusters
        X = np.array(segment_embeddings)
        if X.ndim == 1:
            X = X.reshape(1, -1)
        cluster_labels = self.pipeline.predict(X)

        # Assign cluster IDs
        for i, seg in enumerate(segments):
            seg["speaker_id"] = cluster_labels[i]

        return segments

    def _create_label_mapping(self, train_paths):
        """Create cluster to speaker mapping using training data"""
        train_embeddings = self.embedder.create_embeddings(train_paths)
        train_labels = self.pipeline.predict(train_embeddings)

        from collections import defaultdict
        cluster_speaker_map = defaultdict(list)

        for path, label in zip(train_paths, train_labels):
            speaker = path.split('/')[-2]
            cluster_speaker_map[label].append(speaker)

        for cluster, speakers in cluster_speaker_map.items():
            self.label_map[cluster] = max(set(speakers), key=speakers.count)

    def diarize(self, audio_path, train_paths):
        self._create_label_mapping(train_paths)
        segments = self.process_audio(audio_path)

        for seg in segments:
            cluster_id = seg["speaker_id"]
            seg["speaker_label"] = self.label_map.get(cluster_id, f"Unknown_{cluster_id}")

        return segments

